#escape sequence

# \' ===> Single quote
print("Hi this is \'Python\'")
print("Hey it\'s me")

# \" ===> Double quote
print("Hello, \"Good\" Morning")
print("Whats up\"") 

# \\ ===> Single Backslash
print("Hey This is backslash:\\")
print("This is double backslash printing is Python:\\\\")

# \n ===> Newline character
print("Hello\nGoodevening")

# \t ===> tab 
print("Good\tDay")

# \b ===> backspace
print("Shashikant\bMane")

